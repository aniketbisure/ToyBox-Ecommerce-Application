export const getOrderById = async (req: AuthRequest, res: Response) => {
  try {
    const order = await Order.findById(req.params.id).populate('user', 'name email');

    if (!order) {
      return res.status(404).json({ message: 'Order not found' });
    }

    // Only allow the order owner or an admin to view it
    const userId = req.user?.id;
    const orderUser = order.user as any;
    const isOwner = orderUser && (orderUser._id ? orderUser._id.toString() : orderUser.toString()) === userId;
    const isAdmin = req.user?.role === 'admin';

    if (!isOwner && !isAdmin) {
      return res.status(403).json({ message: 'Not authorized to view this order' });
    }

    res.json(order);
  } catch (error) {
    res.status(500).json({ message: 'Error fetching order', error });
  }
};

// 10/10 LOGIC: Reconciliation Mechanism
// Syncs "PENDING" orders with Razorpay in case webhooks were missed
export const reconcileOrder = async (req: AuthRequest, res: Response) => {
  try {
    const order = await Order.findById(req.params.id);
    if (!order) return res.status(404).json({ message: 'Order not found' });
    if (order.isPaid) return res.json({ message: 'Order already paid', order });

    if (!order.razorpayOrderId) return res.status(400).json({ message: 'No payment intent for this order' });

    // Fetch actual status from Razorpay
    const rzpOrder: any = await razorpay.orders.fetch(order.razorpayOrderId);
    const payments: any = await razorpay.orders.fetchPayments(order.razorpayOrderId);

    const successfulPayment = payments.items.find((p: any) => p.status === 'captured');

    if (successfulPayment) {
      const session = await mongoose.startSession();
      session.startTransaction();
      try {
        order.isPaid = true;
        order.paidAt = new Date();
        order.status = 'PAID';
        order.paymentResult = {
          id: successfulPayment.id,
          status: 'captured',
          update_time: new Date().toISOString(),
          email_address: successfulPayment.email,
        };

        for (const item of order.orderItems) {
          await Product.findOneAndUpdate(
            { _id: item.product, stock: { $gte: item.quantity } },
            { $inc: { stock: -item.quantity } },
            { session }
          );
        }
        await order.save({ session });
        await session.commitTransaction();
        res.json({ message: 'Reconciliation successful: Order marked as PAID', order });
      } catch (err) {
        await session.abortTransaction();
        throw err;
      } finally {
        session.endSession();
      }
    } else {
      res.json({ message: 'Razorpay confirms order is still unpaid', rzpStatus: rzpOrder.status });
    }
  } catch (error: any) {
    res.status(500).json({ message: 'Reconciliation failed', error: error.message });
  }
};
