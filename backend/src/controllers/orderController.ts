export const addOrderItems = async (req: AuthRequest, res: Response) => {
  const {
    orderItems,
    shippingAddress,
    paymentMethod,
    idempotencyKey, // Generated by frontend (e.g., hash of cart + timestamp)
  } = req.body;

  if (orderItems && orderItems.length === 0) {
    res.status(400).json({ message: 'No order items' });
    return;
  }

  try {
    // Idempotency check
    if (idempotencyKey && req.user) {
      const existingOrder = await Order.findOne({ user: req.user.id, idempotencyKey });
      if (existingOrder) {
        return res.status(200).json(existingOrder);
      }
    }

    let calculatedTotalPrice = 0;
    const verifiedOrderItems = [];

    for (const item of orderItems) {
      const dbProduct = await Product.findById(item.product);

      if (!dbProduct) {
        return res.status(404).json({ message: `Product ${item.product} not found` });
      }

      if (dbProduct.stock < item.quantity) {
        return res.status(400).json({ message: `Insufficient stock for ${dbProduct.name}` });
      }

      const itemPrice = dbProduct.price;
      calculatedTotalPrice += itemPrice * item.quantity;

      verifiedOrderItems.push({
        ...item,
        price: itemPrice,
        name: dbProduct.name,
        image: dbProduct.image,
        product: dbProduct._id
      });
    }

    // 1. Create Order in Pending State
    const order = new Order({
      orderItems: verifiedOrderItems,
      user: req.user?.id,
      shippingAddress,
      paymentMethod,
      totalPrice: calculatedTotalPrice,
      isPaid: false,
      idempotencyKey,
      status: 'PENDING'
    });

    // 2. If Razorpay, create Payment Intent
    if (paymentMethod === 'Razorpay') {
      const options = {
        amount: Math.round(calculatedTotalPrice * 100),
        currency: "INR",
        receipt: `receipt_order_${order._id}`,
      };

      const razorpayOrder = await razorpay.orders.create(options);
      order.razorpayOrderId = razorpayOrder.id;
    }

    const createdOrder = await order.save();
    res.status(201).json(createdOrder);
  } catch (error: any) {
    res.status(500).json({ message: 'Error creating order', error: error.message });
  }
};

export const verifyPayment = async (req: AuthRequest, res: Response) => {
  const { razorpay_order_id, razorpay_payment_id, razorpay_signature } = req.body;

  const secret = process.env.RAZORPAY_KEY_SECRET!;
  const hmac = crypto.createHmac('sha256', secret);
  hmac.update(razorpay_order_id + "|" + razorpay_payment_id);
  const generated_signature = hmac.digest('hex');

  if (generated_signature === razorpay_signature) {
    const session = await mongoose.startSession();
    session.startTransaction();

    try {
      const order = await Order.findOne({ razorpayOrderId: razorpay_order_id }).session(session);

      if (order && !order.isPaid) {
        order.isPaid = true;
        order.paidAt = new Date();
        order.status = 'PAID';
        order.paymentResult = {
          id: razorpay_payment_id,
          status: 'success',
          update_time: new Date().toISOString(),
          email_address: '',
        };

        // Decrease stock with safety check
        for (const item of order.orderItems) {
          const product = await Product.findOneAndUpdate(
            { _id: item.product, stock: { $gte: item.quantity } },
            { $inc: { stock: -item.quantity } },
            { session, new: true }
          );

          if (!product) {
            throw new Error(`Insufficient stock for product: ${item.name}`);
          }
        }

        await order.save({ session });
        await session.commitTransaction();
        res.json({ status: 'success', message: 'Payment verified' });
      } else {
        await session.abortTransaction();
        res.status(400).json({ message: 'Order already processed, not found, or payment failed' });
      }
    } catch (error: any) {
      await session.abortTransaction();
      logger.error(`[PAYMENT_ERROR] User: ${req.user?.id} Order: ${razorpay_order_id} Error: ${error.message}`);
      res.status(500).json({ message: error.message || 'Error processing payment' });
    } finally {
      session.endSession();
    }
  } else {
    logger.warn(`[SECURITY_ALERT] Invalid signature attempt for Order: ${razorpay_order_id}`);
    res.status(400).json({ status: 'failure', message: 'Invalid signature' });
  }
};

export const razorpayWebhook = async (req: any, res: Response) => {
  const webhookSecret = process.env.RAZORPAY_WEBHOOK_SECRET;
  if (!webhookSecret) {
    res.status(400).send('Webhook secret not configured');
    return;
  }
  const signature = req.headers['x-razorpay-signature'] as string;

  // SECURITY FIX: Use rawBody for signature verification
  const shasum = crypto.createHmac('sha256', webhookSecret);
  shasum.update(req.rawBody || JSON.stringify(req.body));
  const digest = shasum.digest('hex');

  if (signature === digest) {
    const event = req.body.event;
    if (event === 'payment.captured') {
      const { order_id, id: payment_id } = req.body.payload.payment.entity;

      const session = await mongoose.startSession();
      session.startTransaction();

      try {
        const order = await Order.findOne({ razorpayOrderId: order_id }).session(session);
        if (order && !order.isPaid) {
          order.isPaid = true;
          order.paidAt = new Date();
          order.status = 'PAID';
          order.paymentResult = {
            id: payment_id,
            status: 'captured',
            update_time: new Date().toISOString(),
            email_address: req.body.payload.payment.entity.email,
          };

          for (const item of order.orderItems) {
            const product = await Product.findOneAndUpdate(
              { _id: item.product, stock: { $gte: item.quantity } },
              { $inc: { stock: -item.quantity } },
              { session, new: true }
            );

            if (!product) {
              throw new Error(`Insufficient stock for product: ${item.name}`);
            }
          }
          await order.save({ session });
          await session.commitTransaction();
        } else {
          await session.abortTransaction();
        }
      } catch (error) {
        await session.abortTransaction();
        // Log webhook failure but respond 200 to prevent retry if logic failed
        logger.error('Webhook processing error:', error);
      } finally {
        session.endSession();
      }
    }
    res.status(200).send('OK');
  } else {
    res.status(400).send('Invalid Signature');
  }
};

